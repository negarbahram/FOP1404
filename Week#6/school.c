#include <stdio.h>

int countDigits (int n) {
    int cnt =0;
    while (n >0) {
        n/=10;
        cnt++;
    }
    return cnt;
}

int power(int a, int b) {
    int x = a;
    while (--b>0)
        a*=x;
    return a;
}

int isArmstrong (int n) {
    int sum = 0, cnt = countDigits(n), x = n;
    while (n > 0) {
        int d = n%10;
        sum += power(d, cnt);
        n /= 10;
    }
    return sum == x;
}

int sumDigits (int n) {
    int sum =0;
    while (n >0) {
        sum+= (n%10);
        n/=10;
    }
    return sum;
}

int isCoreOdd (int n) {
    while (n > 9) {
        n = sumDigits(n);
    }
    return (n&1);
}

int main() {
    int n, a[1000];
    scanf("%d", &n);
    for (int i = 0; i < n; ++i) {
        scanf("%d", a+ i);
    }
    int equality = 0;
    for (int i=0; i<n ; i++) {
        if (isCoreOdd(a[i]))
            equality--;
        if (isArmstrong(a[i]))
            equality++;
    }
    if (equality > 0)
        printf("Armstrong");
    else if (equality == 0) {
        printf("Equal");
    }
    else {
        printf("CoreOdd");
    }

    return 0;
}

